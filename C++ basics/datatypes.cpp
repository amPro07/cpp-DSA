#include<iostream>
using namespace std;

int main() {

    int a;
    cout<<"Size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"Size of float "<<sizeof(b)<<endl;

    char c;
    cout<<"Size of char "<<sizeof(c)<<endl;

    bool d;
    cout<<"Size of bool "<<sizeof(d)<<endl;

    long int li;
    short int si;

    cout<<"Size of long int "<<sizeof(li)<<endl;

    cout<<"Size of short int "<<sizeof(si)<<endl;
    
    return 0;
}